exports.postOrder = (req, res, next) => {
  req.user
    .populate("cartAdmin.items.productId")
    .execPopulate()
    .then(user => {
      const products = user.cartAdmin.items.map(i => {
        return { quantity: i.quantity, product: { ...i.productId._doc } };
      });
      // console.log("products", products);
      const order = new Order({
        user: {
          email: req.user.email,
          userId: req.user
        },
        products: products
      });
      return order.save();
    })
    .then(result => {
      return req.user.clearCartAdmin();
    })
    .then(() => {
      res.redirect("/orders");
    })
    .catch(err => {
      const error = new Error(err);
      error.httpStatusCode = 500;
      return next(error);
    });
};

